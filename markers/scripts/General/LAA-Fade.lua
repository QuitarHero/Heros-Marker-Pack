function LAA_Fade(marker, focused)
  local wing, state = 0, not focused
  for _,v in ipairs(HMP.LAA_Trigger) do if(marker.Guid:ToBase64() == v) then wing = _ break end end
  for _,v in ipairs(HMP.LAA_Fade[wing]) do
    if(World:MarkerByGuid(v) == nil) then break
    else World:MarkerByGuid(v).InGameVisibility = state end
  end
end
  
